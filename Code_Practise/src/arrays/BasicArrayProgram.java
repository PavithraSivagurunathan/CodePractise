package arrays;

//BASIC PROGRAM TO PRINT THE VALUE USING TRADITONAL METHOD OF C/C++
public class BasicArrayProgram {
	 public static void main (String[] args) {
		 int [] arr= {1,2,3,4,5};
		 int len = arr.length;
		 for(int loop=0;loop<len;loop++) {
			 System.out.println("Values in array is : "+arr[loop]);
		 }
		 
	 }

}
